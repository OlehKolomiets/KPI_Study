    private int topologyOrder;

    public GreedTopology(int clusterCount) {
        super(cluster.length, clusterCount);
        topologyOrder = (int) Math.sqrt(clusterCount);
    }

    @Override
    public int[][] generateTopology() {

        for (int i = 0; i < clusterCount; i++) {
            copyMatrix(topology, cluster, i * clusterNodes, i * clusterNodes);
        }

        if (topologyOrder == 1) {
            return cluster;
        }

        for (int i = 0; i < topologyOrder; i++) {
            buildHorizontalConnection(i, horizontalConnection, transpose(horizontalConnection));
            buildVerticalConnection(i, verticalConnection, transpose(verticalConnection));
        }

        return topology;
    }

    private void buildHorizontalConnection(int index, int[][] nextConnMatrix, int [][] prevConnMatrix) {
        for (int i = 0; i < topologyOrder; i++) {
            int startPreviousCon = i > 0 ? (index * topologyOrder + i - 1) * clusterNodes : -1;
            int startNextCon = i < topologyOrder - 1 ? (index * topologyOrder + i + 1) * clusterNodes : -1;

            if (startNextCon >= 0) {
                copyMatrix(topology, nextConnMatrix, (index * topologyOrder + i) * clusterNodes, startNextCon);
            }
            if (startPreviousCon >= 0) {
                copyMatrix(topology, prevConnMatrix, (index * topologyOrder + i) * clusterNodes, startPreviousCon);
            }
        }
    }

    private void buildVerticalConnection(int index, int[][] nextConnMatrix, int [][] prevConnMatrix) {
        for (int i = 0; i < topologyOrder; i++) {

            int startPreviousCon = i > 0 ? ((i - 1) * topologyOrder + index) * clusterNodes : -1;
            int startNextCon = i < topologyOrder - 1 ? (index + (i + 1) * topologyOrder) * clusterNodes : -1;

            if (startNextCon != -1) {
                copyMatrix(topology, nextConnMatrix, (index + i * topologyOrder) * clusterNodes, startNextCon);
            }

            if (startPreviousCon != -1) {
                copyMatrix(topology, prevConnMatrix, (index + i * topologyOrder) * clusterNodes, startPreviousCon);
            }
        }
    }
